/* Schema */
/**
 * データの構造を示す
 * ID型はfield値の一意性を担保する
 * エクスクラメーションマーク（！）はフィールドに対しnon-nullであることを強制する。
 * (必須フィールド)
 */
const typeDefs = gql`
  type Person {
    name: String!
    phone: String
    street: String!
    city: String!
    id: ID!
  }

  type Query {
    personCount: Int!
    allPersons: [Person!]!
    findPerson(name: String!): Person
  }
`

/* Resolvers */
// GraphQLクエリに対するレスポンスを定義

const resolvers = {
  Query: {
    personCount: () => persons.length,
    allPersons: () => persons,
    findPerson: (root, args) => persons.find(p => p.name === args.name)
  },
  Person: {
    name: (root) => root.name,
    phone: (root) => root.phone,
    street: (root) => root.street,
    city: (root) => root.city,
    id: (root) => root.id,
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
  playground: {
    version: '1.7.25'
  },
})

server
  .listen()
  .then(({ url }) => {
    console.log(`🚀 SERVER READY AT ${url}`)
  })
